public class A {
    public void public_method() {
        int a = 0;
        while(a < 10) {
            a++;
        }
    }

    protected void protected_method() {
        int a = 0;
        while(a < 10) {
            a++;
        }
    }

    private void private_method() {
        int a = 0;
        while(a < 10) {
            a++;
        }
    }

    public int public_method_with_return_value() {
        int a = 0;
        while(a < 10) {
            a++;
        }

        return 12;
    }
}